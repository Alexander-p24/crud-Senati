    private void btneditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneditarActionPerformed
        // TODO add your handling code here:
       

        
        int selectedRow = tblusuarios.getSelectedRow();
        if (selectedRow == -1) {
            // Si no se ha seleccionado una fila, muestra un mensaje de advertencia
            JOptionPane.showMessageDialog(this, "Por favor, selecciona una fila para editar.");
            return; // Salir del m√©todo si no hay fila seleccionada
        }
        TableModel model = tblusuarios.getModel();

        String id = model.getValueAt(tblusuarios.getSelectedRow(), 0).toString();
        String name = model.getValueAt(tblusuarios.getSelectedRow(), 1).toString();
        String lastname = model.getValueAt(tblusuarios.getSelectedRow(), 2).toString();
        String telefono = model.getValueAt(tblusuarios.getSelectedRow(), 3).toString();

        EditarUsuario formUpd = new EditarUsuario(id, name, lastname, telefono);
        formUpd.setVisible(true);
    }//GEN-LAST:event_btneditarActionPerformed

